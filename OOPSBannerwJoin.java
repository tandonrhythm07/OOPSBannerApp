public class OOPSBannerwJoin {
    public static void main(String[] args) {

        // O      O      P      S
        System.out.println(String.join(" ",
                " *** ", " *** ", " **** ", " ****"));

        System.out.println(String.join(" ",
                "*   *", "*   *", "*   *", "*    "));

        System.out.println(String.join(" ",
                "*   *", "*   *", " **** ", " ****"));

        System.out.println(String.join(" ",
                "*   *", "*   *", "*     ", "    *"));

        System.out.println(String.join(" ",
                " *** ", " *** ", "*     ", " ****"));
    }
}
